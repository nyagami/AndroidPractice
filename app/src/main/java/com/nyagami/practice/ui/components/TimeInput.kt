fun parseTime(hour: Int, minute: Int): String {
    return hour.toString().padStart(2, '0') + ":" + minute.toString().padStart(2, '0')
}
@Composable
fun TimePickerDialog(
    title: String = "Select Time",
    onCancel: () -> Unit,
    onConfirm: () -> Unit,
    toggle: @Composable () -> Unit = {},
    content: @Composable () -> Unit,
) {
    Dialog(
        onDismissRequest = onCancel,
        properties = DialogProperties(
            usePlatformDefaultWidth = false
        ),
    ) {
        Surface(
            shape = MaterialTheme.shapes.extraLarge,
            tonalElevation = 6.dp,
            modifier = Modifier
                .width(IntrinsicSize.Min)
                .height(IntrinsicSize.Min)
                .background(
                    shape = MaterialTheme.shapes.extraLarge,
                    color = MaterialTheme.colorScheme.surface
                ),
        ) {
            Column(
                modifier = Modifier.padding(24.dp),
                horizontalAlignment = Alignment.CenterHorizontally
            ) {
                Text(
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(bottom = 20.dp),
                    text = title,
                    style = MaterialTheme.typography.labelMedium
                )
                content()
                Row(
                    modifier = Modifier
                        .height(40.dp)
                        .fillMaxWidth()
                ) {
                    toggle()
                    Spacer(modifier = Modifier.weight(1f))
                    TextButton(
                        onClick = onCancel
                    ) { Text("Cancel") }
                    TextButton(
                        onClick = onConfirm
                    ) { Text("OK") }
                }
            }
        }
    }
}
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun TimeInput(label: String, state: TimePickerState) {
    var showPicker by remember {
        mutableStateOf(false)
    }
    Row (
        verticalAlignment = Alignment.CenterVertically
    ){
        Text(text = label)
        OutlinedButton(onClick = { showPicker = !showPicker }, Modifier.padding(start = 4.dp)) {
            Text(text = parseTime(state.hour, state.minute))
        }
    }

    if(showPicker){
        TimePickerDialog(onCancel = {showPicker = false}, onConfirm = { showPicker = false }) {
            TimePicker(state = state)
        }
    }
}